#Dynamic Array operation

arr = []
arr.append(10)
arr.append(20)
arr.append(30)

print("After insertion:", arr)
arr.remove(20) 
print("After deletion:", arr)
print("Element at index 1:", arr[1])
arr.append(40)
arr.append(50)
print("After more insertions:", arr)
arr.pop()
print("After popping:", arr)
